/*
* API记录：
*
* children:组件标签内的内容，可以是一个node；默认为：“评分组件”
* readOnly:组件是否只读，
* value:组件的初始化分数，
* theme：组件的主题颜色，可以是组件定义的关键字，也可以是颜色值
* size:组件尺寸
* length:满分（整数）,
* shape:默认"★",评分图案
* hollowShape:默认"☆"，为评分时图案
*
* onRate:评分的执行方法，参数是对应的分数
* */